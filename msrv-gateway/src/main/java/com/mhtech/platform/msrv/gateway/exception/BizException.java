package com.mhtech.platform.msrv.gateway.exception;

/**
 * 基础业务异常
 * @author GM
 *
 */
public class BizException extends RuntimeException {

	private static final long serialVersionUID = 1L;

	private int code;

	protected BizException(int code, String message) {
		super(message);
		this.code = code;
	}

	public int getCode() {
		return code;
	}
}
