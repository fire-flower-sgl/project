package com.mobile.model;

import java.io.Serializable;

/**
 * @ClassName CustomerContact
 * @Description TODO 客户联系人
 * @Author admin
 * @Date 2019/8/5 17:35
 * @Version 1.0
 */
public class CustomerContact implements Serializable{
	/**
	 * id
	 */
	private String id;
	/**
	 * 客户id
	 */
	private String customer_id;
	/**
	 * 联系人
	 */
	private String contact;
	/**
	 * 手机号码
	 */
	private String mobileno;
	/**
	 * 第一联系人
	 */
	private String first_contact;
	/**
	 * 创建事件
	 */
	private String ctime;
	/**
	 * 更新事件
	 */
	private String utime;
	
	
	public CustomerContact() {
		super();
	}
	
	public CustomerContact(String id, String customer_id, String contact, String mobileno, String first_contact,
			String ctime, String utime) {
		super();
		this.id = id;
		this.customer_id = customer_id;
		this.contact = contact;
		this.mobileno = mobileno;
		this.first_contact = first_contact;
		this.ctime = ctime;
		this.utime = utime;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getCustomer_id() {
		return customer_id;
	}

	public void setCustomer_id(String customer_id) {
		this.customer_id = customer_id;
	}

	public String getContact() {
		return contact;
	}

	public void setContact(String contact) {
		this.contact = contact;
	}

	public String getMobileno() {
		return mobileno;
	}

	public void setMobileno(String mobileno) {
		this.mobileno = mobileno;
	}

	public String getFirst_contact() {
		return first_contact;
	}

	public void setFirst_contact(String first_contact) {
		this.first_contact = first_contact;
	}
	
	public String getCtime() {
		return ctime;
	}

	public void setCtime(String ctime) {
		this.ctime = ctime;
	}

	public String getUtime() {
		return utime;
	}

	public void setUtime(String utime) {
		this.utime = utime;
	}

	@Override
	public String toString() {
		return "CustomerContact [id=" + id + ", customer_id=" + customer_id + ", contact=" + contact + ", mobileno="
				+ mobileno + ", first_contact=" + first_contact + ", ctime=" + ctime + ", utime=" + utime + "]";
	}
	

	
	
	
	
	
	

}
