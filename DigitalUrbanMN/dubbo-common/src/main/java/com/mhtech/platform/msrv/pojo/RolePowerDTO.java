package com.mhtech.platform.msrv.pojo;

import java.io.Serializable;

/**
 * 角色权限关联表DTO
 * @author Administrator
 *
 */
public class RolePowerDTO implements Serializable {
	
	

		public RolePowerDTO(String roleNum) {
		super();
		this.roleNum = roleNum;
	}
		private static final long serialVersionUID = 1L;

	    private Long id;
	    private String roleNum;
	    private String powerNum;

		public Long getId() {
			return id;
		}

		public void setId(Long id) {
			this.id = id;
		}

		public String getRoleNum() {
			return roleNum;
		}

		public void setRoleNum(String roleNum) {
			this.roleNum = roleNum;
		}

		public String getPowerNum() {
			return powerNum;
		}

		public void setPowerNum(String powerNum) {
			this.powerNum = powerNum;
		}

		public RolePowerDTO(Long id, String roleNum, String powerNum) {
			super();
			this.id = id;
			this.roleNum = roleNum;
			this.powerNum = powerNum;
		}

		public RolePowerDTO() {
			super();
		}
	    public RolePowerDTO(String roleNum, String powerNum) {
			super();
			this.roleNum = roleNum;
			this.powerNum = powerNum;
		}
}
