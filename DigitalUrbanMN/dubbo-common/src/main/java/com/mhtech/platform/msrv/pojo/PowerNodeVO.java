package com.mhtech.platform.msrv.pojo;

import java.io.Serializable;
import java.util.List;

/**
 * 权限节点
 * @author GM
 */
public class PowerNodeVO implements Serializable{
	private static final long serialVersionUID = 1L;

	private String powerNum;

    private String powerName;

    private String powerFather;
    
    private List<PowerNodeVO> children;

	public String getPowerNum() {
		return powerNum;
	}

	public void setPowerNum(String powerNum) {
		this.powerNum = powerNum;
	}

	public String getPowerName() {
		return powerName;
	}

	public void setPowerName(String powerName) {
		this.powerName = powerName;
	}

	public String getPowerFather() {
		return powerFather;
	}

	public void setPowerFather(String powerFather) {
		this.powerFather = powerFather;
	}

	public List<PowerNodeVO> getChildren() {
		return children;
	}

	public void setChildren(List<PowerNodeVO> children) {
		this.children = children;
	}
}
