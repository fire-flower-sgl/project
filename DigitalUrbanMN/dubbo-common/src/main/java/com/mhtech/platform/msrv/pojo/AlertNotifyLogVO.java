package com.mhtech.platform.msrv.pojo;

import java.io.Serializable;
import java.util.Date;

public class AlertNotifyLogVO implements Serializable{

	/**
	 * 
	 */
	private static final long serialVersionUID = 2775958681547560875L;

	private long id;
	
	private long serverId;
	
	private String username;
	
	private String paramName;
	
	private String usercode;
	
	private String content;
	
	private Integer isSend;
	
	private Integer status;
	
	private Date createdTime;

	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}

	public long getServerId() {
		return serverId;
	}

	public void setServerId(long serverId) {
		this.serverId = serverId;
	}

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

	public String getParamName() {
		return paramName;
	}

	public void setParamName(String paramName) {
		this.paramName = paramName;
	}

	public String getUsercode() {
		return usercode;
	}

	public void setUsercode(String usercode) {
		this.usercode = usercode;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public Integer getIsSend() {
		return isSend;
	}

	public void setIsSend(Integer isSend) {
		this.isSend = isSend;
	}

	public Integer getStatus() {
		return status;
	}

	public void setStatus(Integer status) {
		this.status = status;
	}

	public Date getCreatedTime() {
		return createdTime;
	}

	public void setCreatedTime(Date createdTime) {
		this.createdTime = createdTime;
	}

	public AlertNotifyLogVO() {
		super();
	}

	public AlertNotifyLogVO(long id, long serverId, String username, String paramName, String usercode, String content,
			Integer isSend, Integer status, Date createdTime) {
		super();
		this.id = id;
		this.serverId = serverId;
		this.username = username;
		this.paramName = paramName;
		this.usercode = usercode;
		this.content = content;
		this.isSend = isSend;
		this.status = status;
		this.createdTime = createdTime;
	}

	@Override
	public String toString() {
		return "AlertNotifyLogVO [id=" + id + ", serverId=" + serverId + ", username=" + username + ", paramName="
				+ paramName + ", usercode=" + usercode + ", content=" + content + ", isSend=" + isSend + ", status="
				+ status + ", createdTime=" + createdTime + "]";
	}
	
	
	

}
