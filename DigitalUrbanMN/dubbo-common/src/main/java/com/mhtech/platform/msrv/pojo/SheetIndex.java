package com.mhtech.platform.msrv.pojo;

import java.io.Serializable;

import io.swagger.annotations.ApiModel;
import io.swagger.annotations.ApiModelProperty;

@ApiModel("excel表名")
public class SheetIndex implements Serializable {

	private static final long serialVersionUID = -4635339518468050039L;
	@ApiModelProperty("索引位")
	private int index;
	@ApiModelProperty("名称")
	private String sheetName;
	public SheetIndex() {}
	public SheetIndex(int index, String sheetName) {
		super();
		this.index = index;
		this.sheetName = sheetName;
	}

	public int getIndex() {
		return index;
	}
	public void setIndex(int index) {
		this.index = index;
	}
	public String getSheetName() {
		return sheetName;
	}
	public void setSheetName(String sheetName) {
		this.sheetName = sheetName;
	}
}
