package com.mhtech.platform.msrv.pojo;

import java.io.Serializable;

public class HardwareTypeDTO implements Serializable{

	/**
	 * 
	 */
	private static final long serialVersionUID = 8631055582898510349L;

	private int pageSize;

	private int pageNo;

	private String typeCode;

	private String typeAlias;

	private String unit;

	private String memo;

	public int getPageSize() {
		return pageSize;
	}

	public void setPageSize(int pageSize) {
		this.pageSize = pageSize;
	}

	public int getPageNo() {
		return pageNo;
	}

	public void setPageNo(int pageNo) {
		this.pageNo = pageNo;
	}

	public String getTypeCode() {
		return typeCode;
	}

	public void setTypeCode(String typeCode) {
		this.typeCode = typeCode;
	}

	public String getTypeAlias() {
		return typeAlias;
	}

	public void setTypeAlias(String typeAlias) {
		this.typeAlias = typeAlias;
	}

	public String getUnit() {
		return unit;
	}

	public void setUnit(String unit) {
		this.unit = unit;
	}

	public String getMemo() {
		return memo;
	}

	public void setMemo(String memo) {
		this.memo = memo;
	}

	public HardwareTypeDTO(String typeCode, String typeAlias, String unit, String memo) {
		super();
		this.typeCode = typeCode;
		this.typeAlias = typeAlias;
		this.unit = unit;
		this.memo = memo;
	}

	public HardwareTypeDTO() {
		super();
	}
	
	

}
