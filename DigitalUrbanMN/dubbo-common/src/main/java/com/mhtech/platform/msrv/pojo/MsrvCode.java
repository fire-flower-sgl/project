package com.mhtech.platform.msrv.pojo;

public enum MsrvCode {

	TRANSMISSION("10", "传输中"),
	COMPLETE("20","已完成");

	
	private String code;
	
	private String message;

	private MsrvCode(String code, String message) {
		this.code = code;
		this.message = message;
	}

	public String getCode() {
		return code;
	}

	public String getMessage() {
		return message;
	}
}
