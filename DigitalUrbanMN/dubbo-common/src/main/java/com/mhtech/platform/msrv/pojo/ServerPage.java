package com.mhtech.platform.msrv.pojo;

import java.io.Serializable;
import java.util.List;

public class ServerPage implements Serializable {

	private static final long serialVersionUID = 8438358495631221814L;

	private int pageSize =10; //每页显示条数
    private int totalCount; //总条数
    private int start; //开始条数
    private int pageNo;//当前页
    private int totalPages; //总页数
    
    private List<ServerAdminInfo> servers;

	public int getPageSize() {
		return pageSize;
	}

	public void setPageSize(int pageSize) {
		this.pageSize = pageSize;
	}

	public int getTotalCount() {
		return totalCount;
	}

	public void setTotalCount(int totalCount) {
		this.totalCount = totalCount;
	}

	public int getStart() {
		return start;
	}

	public void setStart(int start) {
		this.start = start;
	}

	public int getPageNo() {
		return pageNo;
	}

	public void setPageNo(int pageNo) {
		this.pageNo = pageNo;
	}

	public int getTotalPages() {
		return totalPages;
	}

	public void setTotalPages(int totalPages) {
		this.totalPages = totalPages;
	}

	public List<ServerAdminInfo> getServers() {
		return servers;
	}

	public void setServers(List<ServerAdminInfo> servers) {
		this.servers = servers;
	}
}
